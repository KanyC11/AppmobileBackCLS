<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Evenement extends Model
{
    protected $table = 'sn_evenements';

    protected $fillable = [
        'libelle',
        'description',
        'date_debut',
        'date_fin',
        'heure_debut',
        'heure_fin',
        'type',
        'lieu',
        'lien',
        'image'
    ];

    public function intervenants()
    {
        return $this->belongsToMany(
            Intervenant::class,
            'sn_evenement_intervenant',
            'evenement_id',
            'intervenant_id'
        );
    }
}
